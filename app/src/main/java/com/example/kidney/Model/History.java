package com.example.kidney.Model;

public class History {
    private String duration, drowsiness, change, hands, ex_protein, urinary, urinary_color, urinary_volume, hospital_history , date;

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getDuration() {
        return duration;
    }

    public void setDuration(String duration) {
        this.duration = duration;
    }

    public String getDrowsiness() {
        return drowsiness;
    }

    public void setDrowsiness(String drowsiness) {
        this.drowsiness = drowsiness;
    }

    public String getChange() {
        return change;
    }

    public void setChange(String change) {
        this.change = change;
    }

    public String getHands() {
        return hands;
    }

    public void setHands(String hands) {
        this.hands = hands;
    }

    public String getEx_protein() {
        return ex_protein;
    }

    public void setEx_protein(String ex_protein) {
        this.ex_protein = ex_protein;
    }

    public String getUrinary() {
        return urinary;
    }

    public void setUrinary(String urinary) {
        this.urinary = urinary;
    }

    public String getUrinary_color() {
        return urinary_color;
    }

    public void setUrinary_color(String urinary_color) {
        this.urinary_color = urinary_color;
    }

    public String getUrinary_volume() {
        return urinary_volume;
    }

    public void setUrinary_volume(String urinary_volume) {
        this.urinary_volume = urinary_volume;
    }

    public String getHospital_history() {
        return hospital_history;
    }

    public void setHospital_history(String hospital_history) {
        this.hospital_history = hospital_history;
    }
}
